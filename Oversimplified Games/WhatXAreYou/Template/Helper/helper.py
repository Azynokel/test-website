def add_to_string_digits(input_string: str) -> str:
    result = ""
    for char in input_string:
        digit = int(char)
        new_digit = 1

        match digit:
            case 0: new_digit = 0
            case 1: new_digit = 0
            case 2: new_digit = 1
            case 3: new_digit = 2

        result += str(new_digit)
    return result

def sum_columns(s1, s2, s3):
    nums1 = list(map(int, s1))
    nums2 = list(map(int, s2))
    nums3 = list(map(int, s3))
    
    result = []
    
    for a, b, c in zip(nums1, nums2, nums3):
        result.append(str(a + b + c))
    
    return ''.join(result)

